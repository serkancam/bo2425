#include <stdio.h>

int main()
{
    //bitwise(bit düzeyi) operatörler
    // işleme girenb sayılar bitlere açılıp bit bit işlem yapılır.
    int a=5,b=11;
    printf("a bve b=%d\n",a&b);
    printf("a bveya b=%d\n",a|b);
    printf("a böveya b=%d\n",a^b);
    printf("değil b=%d\n",~b);


    return 0;


}
